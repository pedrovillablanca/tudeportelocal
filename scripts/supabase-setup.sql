-- Habilitar extensión unaccent para búsqueda sin acentos
CREATE EXTENSION IF NOT EXISTS unaccent;

-- Función para búsqueda sin acentos (case-insensitive + accent-insensitive)
CREATE OR REPLACE FUNCTION search_clubs(query_text TEXT)
RETURNS TABLE (
  id UUID,
  name TEXT,
  slug TEXT,
  description TEXT,
  region_id INTEGER,
  commune_id INTEGER,
  address TEXT,
  instagram_url TEXT,
  facebook_url TEXT,
  contact_email TEXT,
  contact_phone TEXT,
  status TEXT,
  is_featured BOOLEAN,
  is_deleted BOOLEAN,
  created_at TIMESTAMPTZ,
  updated_at TIMESTAMPTZ
) AS $$
BEGIN
  RETURN QUERY
  SELECT 
    c.id,
    c.name,
    c.slug,
    c.description,
    c.region_id,
    c.commune_id,
    c.address,
    c.instagram_url,
    c.facebook_url,
    c.contact_email,
    c.contact_phone,
    c.status,
    c.is_featured,
    c.is_deleted,
    c.created_at,
    c.updated_at
  FROM clubs c
  WHERE c.status = 'active'
    AND c.is_deleted = false
    AND unaccent(LOWER(c.name)) LIKE '%' || unaccent(LOWER(query_text)) || '%'
  ORDER BY c.is_featured DESC, c.name ASC;
END;
$$ LANGUAGE plpgsql;

-- Tabla: Regiones de Chile
CREATE TABLE IF NOT EXISTS regions (
  id INTEGER PRIMARY KEY,
  name VARCHAR(100) NOT NULL UNIQUE,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabla: Comunas
CREATE TABLE IF NOT EXISTS communes (
  id INTEGER PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  region_id INTEGER NOT NULL REFERENCES regions(id) ON DELETE CASCADE,
  slug VARCHAR(100) NOT NULL UNIQUE,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(name, region_id)
);

-- Tabla: Deportes
CREATE TABLE IF NOT EXISTS sports (
  id INTEGER PRIMARY KEY,
  name VARCHAR(100) NOT NULL UNIQUE,
  slug VARCHAR(100) NOT NULL UNIQUE,
  icon VARCHAR(50),
  is_active BOOLEAN DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabla: Clubes
CREATE TABLE IF NOT EXISTS clubs (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name VARCHAR(200) NOT NULL,
  slug VARCHAR(200) NOT NULL UNIQUE,
  description TEXT,
  region_id INTEGER NOT NULL REFERENCES regions(id),
  commune_id INTEGER NOT NULL REFERENCES communes(id),
  address VARCHAR(300),
  instagram_url VARCHAR(500),
  facebook_url VARCHAR(500),
  contact_email VARCHAR(255) NOT NULL,
  contact_phone VARCHAR(50),
  status VARCHAR(20) DEFAULT 'pending' CHECK (status IN ('pending', 'active', 'inactive', 'rejected')),
  is_featured BOOLEAN DEFAULT false,
  is_deleted BOOLEAN DEFAULT false,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabla: Relación Club-Deportes (muchos a muchos)
CREATE TABLE IF NOT EXISTS club_sports (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  club_id UUID NOT NULL REFERENCES clubs(id) ON DELETE CASCADE,
  sport_id INTEGER NOT NULL REFERENCES sports(id),
  is_primary BOOLEAN DEFAULT false,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(club_id, sport_id)
);

-- Tabla: Solicitudes de Cambio
CREATE TABLE IF NOT EXISTS update_requests (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  club_id UUID NOT NULL REFERENCES clubs(id) ON DELETE CASCADE,
  type VARCHAR(50) NOT NULL CHECK (type IN ('info_change', 'social_update', 'sports_change', 'club_closed', 'remove_profile', 'other')),
  description TEXT,
  contact_email VARCHAR(255) NOT NULL,
  status VARCHAR(20) DEFAULT 'pending' CHECK (status IN ('pending', 'applied', 'rejected')),
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- ============================================
-- Índices para rendimiento
-- ============================================
CREATE INDEX IF NOT EXISTS idx_clubs_status ON clubs(status) WHERE is_deleted = false;
CREATE INDEX IF NOT EXISTS idx_clubs_region ON clubs(region_id);
CREATE INDEX IF NOT EXISTS idx_clubs_commune ON clubs(commune_id);
CREATE INDEX IF NOT EXISTS idx_clubs_slug ON clubs(slug);
CREATE INDEX IF NOT EXISTS idx_clubs_is_featured ON clubs(is_featured) WHERE is_deleted = false;
CREATE INDEX IF NOT EXISTS idx_communes_region ON communes(region_id);
CREATE INDEX IF NOT EXISTS idx_club_sports_club ON club_sports(club_id);
CREATE INDEX IF NOT EXISTS idx_club_sports_sport ON club_sports(sport_id);
CREATE INDEX IF NOT EXISTS idx_update_requests_club ON update_requests(club_id);
CREATE INDEX IF NOT EXISTS idx_update_requests_status ON update_requests(status);

